/**
 * Add a content hash to the generated file header.
 * The hash covers the file content AFTER the header line so it's stable.
 */
export function addContentHash(content: string, commentPrefix = "//"): string {
  const hash = createHash("sha256").update(content).digest("hex").slice(0, 12);
  const hashLine = `${commentPrefix} Content hash: ${hash}`;

  // Find the auto-generated marker line and insert hash after it
  const marker = "This file is auto-generated by @archastro/sdk-generator. Do not edit.";
  const markerPatterns = [
    `// ${marker}`,
    `# // ${marker}`,
    `# ${marker}`,
  ];

  for (const pattern of markerPatterns) {
    if (content.includes(pattern)) {
      return content.replace(pattern, `${pattern}\n${hashLine}`);
    }
  }

  // Fallback: prepend copyright + header + hash
  const copyright = `${commentPrefix} Copyright (c) 2026 ArchAstro Inc. All Rights Reserved.`;
  const headerLine = `${commentPrefix} ${marker}`;
  return `${copyright}\n${headerLine}\n${hashLine}\n\n${content}`;
}

const GENERATED_MARKER = "This file is auto-generated by @archastro/sdk-generator. Do not edit.";

/**
 * Delete auto-generated files in `dirs` that are not in the new `files` map.
 * Only deletes files containing the auto-generated marker header.
 */
export function cleanStaleFiles(
  files: Record<string, string>,
  dirs: string[],
  extensions: string[]
): void {
  const newPaths = new Set(Object.keys(files));
  const extSet = new Set(extensions);

  for (const dir of dirs) {
    let entries: string[];
    try {
      entries = readdirSync(dir);
    } catch {
      continue; // dir doesn't exist yet
    }

    for (const entry of entries) {
      const fullPath = join(dir, entry);
      try {
        if (!statSync(fullPath).isFile()) continue;
      } catch {
        continue;
      }
      const ext = entry.substring(entry.lastIndexOf("."));
      if (!extSet.has(ext)) continue;
      if (newPaths.has(fullPath)) continue;

      // Only delete files we previously generated
      try {
        const head = readFileSync(fullPath, "utf-8").slice(0, 200);
        if (head.includes(GENERATED_MARKER)) {
          unlinkSync(fullPath);
        }
      } catch {
        // skip unreadable files
      }
    }
  }
}
