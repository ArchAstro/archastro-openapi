/**
 * Code generation utilities — indentation, import tracking, file writing.
 */

import { createHash } from "node:crypto";
import { readdirSync, readFileSync, unlinkSync, statSync } from "node:fs";
import { join } from "node:path";

export class CodeBuilder {
  private lines: string[] = [];
  private indentLevel = 0;
  private indentStr: string;

  constructor(indentStr = "  ") {
    this.indentStr = indentStr;
  }

  indent(): this {
    this.indentLevel++;
    return this;
  }

  dedent(): this {
    this.indentLevel = Math.max(0, this.indentLevel - 1);
    return this;
  }

  line(text = ""): this {
    if (text === "") {
      this.lines.push("");
    } else {
      this.lines.push(this.indentStr.repeat(this.indentLevel) + text);
    }
    return this;
  }

  block(header: string, fn: () => void): this {
    this.line(header + " {");
    this.indent();
    fn();
    this.dedent();
    this.line("}");
    return this;
  }

  /** Python-style block: header with colon, indented body, no closing brace. */
  pyBlock(header: string, fn: () => void): this {
    this.line(header + ":");
    this.indent();
    fn();
    this.dedent();
    return this;
  }

  toString(): string {
    return this.lines.join("\n") + "\n";
  }
}

/**
 * Tracks imports and emits a sorted import block.
 */
export class ImportTracker {
  private imports = new Map<string, Set<string>>();
  private typeImports = new Map<string, Set<string>>();

  add(module: string, ...names: string[]): this {
    const existing = this.imports.get(module) ?? new Set();
    for (const name of names) existing.add(name);
    this.imports.set(module, existing);
    return this;
  }

  addType(module: string, ...names: string[]): this {
    const existing = this.typeImports.get(module) ?? new Set();
    for (const name of names) existing.add(name);
    this.typeImports.set(module, existing);
    return this;
  }

  emit(): string {
    const lines: string[] = [];

    for (const [module, names] of sortedEntries(this.imports)) {
      const sorted = [...names].sort();
      lines.push(`import { ${sorted.join(", ")} } from "${module}";`);
    }

    for (const [module, names] of sortedEntries(this.typeImports)) {
      const sorted = [...names].sort();
      lines.push(`import type { ${sorted.join(", ")} } from "${module}";`);
    }

    return lines.length > 0 ? lines.join("\n") + "\n" : "";
  }
}

function sortedEntries<V>(map: Map<string, V>): [string, V][] {
  return [...map.entries()].sort(([a], [b]) => a.localeCompare(b));
}

/**
 * Header comment for generated files (placeholder — hash added later).
 */
export function generatedHeader(): string {
  return `// Copyright (c) 2026 ArchAstro Inc. All Rights Reserved.\n// This file is auto-generated by @archastro/sdk-generator. Do not edit.\n`;
}

/**
 * Emit a Python import line, wrapping to multi-line if it exceeds the line length limit.
 */
export function emitPythonImportLine(cb: CodeBuilder, mod: string, names: string[], lineLength = 100): void {
  const oneLiner = `from ${mod} import ${names.join(", ")}`;
  if (oneLiner.length <= lineLength) {
    cb.line(oneLiner);
  } else {
    cb.line(`from ${mod} import (`);
    for (const name of names) {
      cb.line(`    ${name},`);
    }
    cb.line(")");
  }
}

